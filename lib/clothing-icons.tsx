// ─── Piece → Icon Matcher ──────────────────────────────────────────────────────
export function matchPieceType(text: string): string {
  const p = text.toLowerCase()
  // Dresses (specific before generic)
  if (p.match(/maxi|floor.length|long (dress|gown)/))           return 'Maxi Dress'
  if (p.match(/midi/))                                           return 'Midi Dress'
  if (p.match(/mini|shift dress|bodycon|slip dress/))           return 'Mini Dress'
  if (p.match(/jumpsuit|romper|playsuit/))                      return 'Midi Dress'
  if (p.match(/dress|gown|sundress/))                           return 'Midi Dress'
  // Bottoms
  if (p.match(/skirt/))                                         return 'Skirt'
  if (p.match(/legging/))                                       return 'Leggings'
  if (p.match(/shorts?/))                                       return 'Shorts'
  if (p.match(/jeans?|denim (pant|trouser)/))                   return 'Jeans'
  if (p.match(/trouser|chino|slack|dress pant|wide.leg/))       return 'Trousers'
  if (p.match(/pant\b/))                                        return 'Trousers'
  // Outerwear
  if (p.match(/blazer/))                                        return 'Blazer'
  if (p.match(/coat|trench|overcoat|peacoat|duster/))           return 'Coat'
  if (p.match(/leather jacket|bomber|moto|biker|denim jacket/)) return 'Jacket'
  if (p.match(/jacket/))                                        return 'Jacket'
  if (p.match(/cardigan/))                                      return 'Cardigan'
  // Tops
  if (p.match(/hoodie|sweatshirt/))                             return 'Hoodie'
  if (p.match(/sweater|knit|knitwear|pullover|turtleneck|cashmere|ribbed/)) return 'Sweater'
  if (p.match(/blouse/))                                        return 'Blouse'
  if (p.match(/button.?(down|up|front)|oxford shirt|linen shirt|dress shirt/)) return 'Button Shirt'
  if (p.match(/tank|cami|spaghetti strap|crop top/))           return 'Tank Top'
  if (p.match(/t.?shirt|tee\b|polo/))                          return 'T-Shirt'
  if (p.match(/top\b|shirt\b/))                                 return 'T-Shirt'
  // Shoes
  if (p.match(/boots?|ankle boot|knee.high/))                   return 'Boots'
  if (p.match(/heel|pump|stiletto|mule|kitten heel|wedge/))     return 'Heels'
  if (p.match(/sneaker|trainer|runner|athletic shoe|canvas shoe|sport shoe/)) return 'Sneakers'
  if (p.match(/loafer|flat\b|ballet flat|oxford|derby|monk shoe|moccasin/))  return 'Loafers'
  if (p.match(/sandal|slide\b|flip.?flop|espadrille/))          return 'Sandals'
  // Accessories
  if (p.match(/backpack/))                                      return 'Backpack'
  if (p.match(/bag|purse|clutch|tote|handbag|satchel|crossbody|shoulder bag/)) return 'Handbag'
  if (p.match(/scarf|wrap/))                                    return 'Scarf'
  if (p.match(/belt/))                                          return 'Belt'
  // Default
  return 'T-Shirt'
}

