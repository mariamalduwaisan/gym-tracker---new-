export function swatchColor(colorName: string): string {
  const k = colorName.toLowerCase().trim()
  if (FASHION_COLORS[k]) return FASHION_COLORS[k]
  // Partial match — longer keys first to avoid greedy short-key matches
  const sorted = Object.entries(FASHION_COLORS).sort((a, b) => b[0].length - a[0].length)
  for (const [key, val] of sorted) {
    if (k.includes(key) || key.includes(k)) return val
  }
  return '#C8BDB6' // warm fallback
}

